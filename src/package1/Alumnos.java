    private void cargarTabla() {
        try {
            DefaultTableModel modeloTabla = (DefaultTableModel) tblAlumnos.getModel();
            modeloTabla.setRowCount(0); // Reinicia todas las filas existentes
            PreparedStatement ps;
            ResultSet rs;
            ResultSetMetaData rsmd;
            int[] anchos = {50, 50, 10, 8, 130};
            for (int i = 0; i < tblAlumnos.getColumnCount(); i++) {
                tblAlumnos.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }
            Connection conexion = Conexion.getConnection();
            ps = conexion.prepareStatement("SELECT Nombre, Apellido, DNI, Edad, Email FROM Alumnos");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            int columnas = rsmd.getColumnCount(); // Cuantas columnas trae la consulta ant.

            while (rs.next()) {
                Object[] fila = new Object[columnas];
                for (int i = 0; i < columnas; i++) {
                    fila[i] = rs.getObject(i + 1); // + 1 porque al índice lo inicializamos en 0. Pero en un registro SQL se inicia en 1
                }
                modeloTabla.addRow(fila); // Para asignar la fila construida anteriormente
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }

