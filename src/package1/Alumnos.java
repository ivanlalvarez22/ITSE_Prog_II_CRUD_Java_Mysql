    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            int dni = Integer.parseInt(txtDni.getText());
            int edad = Integer.parseInt(txtEdad.getText());
            String email = txtEmail.getText();
            if (!camposCompletos()) {
                JOptionPane.showMessageDialog(null, "¡Campos Vacíos!");
                return;
            } else if (!txtEmail.getText().contains("@") || !txtEmail.getText().contains(".")) {
                JOptionPane.showMessageDialog(null, "Correo electrónico inválido");
                return;
            }
            Connection conexion = Conexion.getConnection();
            PreparedStatement ps = conexion.prepareStatement("INSERT INTO Alumnos (Nombre, Apellido, DNI, Edad, Email) VALUES (?, ?, ?, ?, ?)");
            ps.setString(1, nombre);
            ps.setString(2, apellido);
            ps.setInt(3, dni);
            ps.setInt(4, edad);
            ps.setString(5, email);
            ps.executeUpdate();
            limpiar();
            cargarTabla();
            JOptionPane.showMessageDialog(null, "¡Registro guardado con éxito!");
            ps.close();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Campos vacíos o formato inválido para edad o dni");
        } catch (SQLException e) {
            limpiar();
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

